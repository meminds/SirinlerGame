
public class Mantar {
    
    private int taneDegeri = 50;
    private int x ;
    private int y;

    public Mantar() {
    }
    
    

    public Mantar(int x, int y) {
        this.x = x;
        this.y = y;
    }

    
    
    public int getTaneDegeri() {
        return taneDegeri;
    }

    public void setTaneDegeri(int taneDegeri) {
        this.taneDegeri = taneDegeri;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    
    
    
}
