
public class Altın {
    private int taneDegeri = 5;
    private int altinSayisi=5;
    private int x ;
    private int y;

    public Altın(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    

    public int getTaneDegeri() {
        return taneDegeri;
    }

    public void setTaneDegeri(int taneDegeri) {
        this.taneDegeri = taneDegeri;
    }

    public int getAltinSayisi() {
        return altinSayisi;
    }

    public void setAltinSayisi(int altinSayisi) {
        this.altinSayisi = altinSayisi;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    
    
    
    
}
